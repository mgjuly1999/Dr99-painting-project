package com.coder.apiForm;

public class LiteragePrice {
	
	private String literageId;
	private String literage;
	private double unitPrice;
	
	public String getLiterage() {
		return literage;
	}
	public void setLiterage(String literage) {
		this.literage = literage;
	}
	public double getUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(double unitPrice) {
		this.unitPrice = unitPrice;
	}
	public String getLiterageId() {
		return literageId;
	}
	public void setLiterageId(String literageId) {
		this.literageId = literageId;
	}

}
