package com.coder.form;

import com.coder.model.ColorGroup;

public class ColorForm {
	
	private String colorGroupId;
	private String htmlColorCode;
	private String colorName;
	private String colorNumber;
	private String description;
	
	public String getColorGroupId() {
		return colorGroupId;
	}
	public void setColorGroupId(String colorGroupId) {
		this.colorGroupId = colorGroupId;
	}
	public String getHtmlColorCode() {
		return htmlColorCode;
	}
	public void setHtmlColorCode(String htmlColorCode) {
		this.htmlColorCode = htmlColorCode;
	}
	public String getColorName() {
		return colorName;
	}
	public void setColorName(String colorName) {
		this.colorName = colorName;
	}
	public String getColorNumber() {
		return colorNumber;
	}
	public void setColorNumber(String colorNumber) {
		this.colorNumber = colorNumber;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
}
