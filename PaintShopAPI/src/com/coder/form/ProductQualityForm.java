package com.coder.form;

public class ProductQualityForm {
	
	private String productQualityId;
	private String name;
	private String description;
	
	public String getProductQualityId() {
		return productQualityId;
	}
	public void setProductQualityId(String productQualityId) {
		this.productQualityId = productQualityId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
}
